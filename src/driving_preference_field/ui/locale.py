def normalize_language(language: str | None) -> str:
    if language in SUPPORTED_LANGUAGES:
        return str(language)
    return DEFAULT_LANGUAGE


def t(language: str, key: str, **kwargs: object) -> str:
    lang = normalize_language(language)
    text = UI_TEXTS.get(lang, {}).get(key)
    if text is None:
        text = UI_TEXTS[DEFAULT_LANGUAGE].get(key, key)
    return text.format(**kwargs)


def language_display_items() -> tuple[tuple[str, str], ...]:
    return (
        (LANG_EN, UI_TEXTS[LANG_EN]["language.english"]),
        (LANG_KO, UI_TEXTS[LANG_KO]["language.korean"]),
    )


def docs_root(repo_root: Path, language: str) -> Path:
    return repo_root / "docs" / normalize_language(language)


def guide_doc_path(repo_root: Path, language: str) -> Path:
    return docs_root(repo_root, language) / "how-to" / "parameter_lab.md"


def language_readme_path(repo_root: Path, language: str) -> Path:
    lang = normalize_language(language)
    return repo_root / ("README.md" if lang == LANG_EN else "README.ko.md")
